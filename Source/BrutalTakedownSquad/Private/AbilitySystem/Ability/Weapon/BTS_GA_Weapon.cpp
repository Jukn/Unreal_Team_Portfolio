void UBTS_GA_Weapon::OnGiveAbility(const FGameplayAbilityActorInfo* ActorInfo, const FGameplayAbilitySpec& Spec)
{
	Super::OnGiveAbility(ActorInfo, Spec);

	CharacterBase = CastChecked<ABTS_CharacterBase>(ActorInfo->AvatarActor.Get());
}

void UBTS_GA_Weapon::OnRemoveAbility(const FGameplayAbilityActorInfo* ActorInfo, const FGameplayAbilitySpec& Spec)
{
	Super::OnRemoveAbility(ActorInfo, Spec);

	CharacterBase = nullptr;
}

